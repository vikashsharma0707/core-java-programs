// inrement and decrement operator
class A1{
      public static void main(String args[]){
      int a,b;
      a=45;
      b=90;
    
      // increment operator
     ++a;
     System.out.println("A :" +a);
     a++;
     System.out.println("A :" +a);
  
    // decrement operator
     b--;
     System.out.println("B :" +b);
  
    --b;
     System.out.println("B :" +b);
    
   }
 }